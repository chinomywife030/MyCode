let toastRef: ToastContextType | null = null;

export function setToastRef(ref: ToastContextType) {
  toastRef = ref;
}

export function toast(type: ToastType, message: string, duration?: number) {
  if (toastRef) {
    toastRef.showToast(type, message, duration);
  } else {
    console.warn('[Toast] ToastProvider not initialized');
  }
}

